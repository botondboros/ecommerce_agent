#!/usr/bin/env python3
"""
build_results.py
────────────────
Reads decision.json + bot_results.json + bot_instructions.json
and embeds them directly into grocery_results.html → grocery_results_view.html

This solves the file:// CORS issue — the output file works when opened directly.

Usage:
    python build_results.py
    start grocery_results_view.html
"""

import json, os, sys, subprocess
from pathlib import Path

SCRIPT_DIR = Path(__file__).parent

def load_json(filename):
    path = SCRIPT_DIR / filename
    if path.exists():
        try:
            return json.loads(path.read_text(encoding='utf-8'))
        except Exception as e:
            print(f"  ⚠ Could not read {filename}: {e}")
    return None

def main():
    print("Building grocery_results_view.html...")

    decision     = load_json('decision.json')
    bot_results  = load_json('bot_results.json')
    instructions = load_json('bot_instructions.json')

    if not decision:
        print("✗ decision.json not found. Run python run.py first.")
        sys.exit(1)

    template_path = SCRIPT_DIR / 'grocery_results.html'
    if not template_path.exists():
        print("✗ grocery_results.html not found.")
        sys.exit(1)

    html = template_path.read_text(encoding='utf-8')

    # Inject data as inline JS before the closing </script>
    inject = f"""
// ── Injected by build_results.py ─────────────────────────────────────────
window._DECISION     = {json.dumps(decision,     ensure_ascii=False)};
window._BOT_RESULTS  = {json.dumps(bot_results,  ensure_ascii=False)};
window._INSTRUCTIONS = {json.dumps(instructions, ensure_ascii=False)};
"""

    # Replace the loadData() function to use injected data instead of fetch
    old_load = """async function loadData() {
  const app = document.getElementById('app');
  app.innerHTML = `<div class="empty"><div class="empty-icon">⏳</div>
    <div class="empty-title">Loading…</div></div>`;

  try {
    // Try to load decision.json (generated by grocery_agent.py)
    const [decRes, botRes, instrRes] = await Promise.all([
      fetch('decision.json').catch(()=>null),
      fetch('bot_results.json').catch(()=>null),
      fetch('bot_instructions.json').catch(()=>null),
    ]);

    if (!decRes || !decRes.ok) {
      app.innerHTML = `
        <div class="empty">
          <div class="empty-icon">📋</div>
          <div class="empty-title">No results yet</div>
          <div class="empty-desc">
            Run the grocery agent first:<br>
            <code style="font-family:var(--fm);font-size:11px;background:#f0ebe2;padding:3px 8px;border-radius:5px;margin-top:8px;display:inline-block">
              python run.py "pasta, tej, tojás..."
            </code>
          </div>
        </div>`;
      return;
    }

    const decision     = await decRes.json();
    const botResults   = botRes?.ok  ? await botRes.json()  : null;
    const instructions = instrRes?.ok ? await instrRes.json() : null;

    render(decision, botResults, instructions);

  } catch(e) {
    app.innerHTML = `<div class="empty">
      <div class="empty-icon">⚠️</div>
      <div class="empty-title">Error loading data</div>
      <div class="empty-desc">${e.message}<br>Make sure decision.json is in the same folder.</div>
    </div>`;
  }
}"""

    new_load = """async function loadData() {
  try {
    const decision     = window._DECISION;
    const botResults   = window._BOT_RESULTS;
    const instructions = window._INSTRUCTIONS;
    if (!decision) throw new Error('No data injected');
    render(decision, botResults, instructions);
  } catch(e) {
    document.getElementById('app').innerHTML = `<div class="empty">
      <div class="empty-icon">⚠️</div>
      <div class="empty-title">Error loading data</div>
      <div class="empty-desc">${e.message}</div>
    </div>`;
  }
}"""

    # Always inject data + override loadData — works regardless of template version
    override_script = f"""
{inject}
// Override loadData to use injected data
window.loadData = async function() {{
  try {{
    if (!window._DECISION) throw new Error('No data');
    render(window._DECISION, window._BOT_RESULTS, window._INSTRUCTIONS);
  }} catch(e) {{
    document.getElementById('app').innerHTML = '<div class="empty"><div class="empty-icon">⚠️</div><div class="empty-title">Error: ' + e.message + '</div></div>';
  }}
}};
// Auto-run
if (document.readyState === 'loading') {{
  document.addEventListener('DOMContentLoaded', loadData);
}} else {{
  loadData();
}}
"""
    html = html.replace('</body>', f'<script>\n{override_script}\n</script>\n</body>')
    # Remove the original loadData() call to avoid double-run
    html = html.replace('\nloadData();\n', '\n// loadData called by injected script\n')

    output = SCRIPT_DIR / 'grocery_results_view.html'
    output.write_text(html, encoding='utf-8')
    print(f"✓ Written: {output}")

    # Open in browser
    print("  Opening in browser...")
    if sys.platform == 'win32':
        os.startfile(str(output))
    elif sys.platform == 'darwin':
        subprocess.run(['open', str(output)])
    else:
        subprocess.run(['xdg-open', str(output)])

if __name__ == '__main__':
    main()
